import random

# ---------- ФУНКЦІЇ ----------

def get_random_joke(category):
    """
    Повертає випадковий жарт з вибраної категорії.
    Якщо користувач ввів неіснуючу категорію — повертає повідомлення про помилку.
    """
    jokes = {
        "it": [
            "Як зрозуміти, що програміст закохався? Він починає писати код без помилок.",
            "Помилка 404: мотивація не знайдена.",
            "Коли ти кажеш 'ще одна фіча', а в результаті — ще один баг.",
            "Справжній програміст не боїться темряви — у нього темна тема.",
            "Я не п'ю каву, я просто оновлюю систему."
        ],
        "школа": [
            "Вчителька: 'Хто хоче 12 балів?' — усі мовчать, бо думають, що це пастка.",
            "Коли вчитель каже: 'Контрольна буде легкою' — це вже тривожний дзвінок.",
            "Онлайн-урок — коли камера вимкнена, мікрофон вимкнений, і ти теж вимкнений.",
            "Домашка — це спосіб нагадати дітям, що життя — боротьба.",
            "Математика в житті не знадобиться... доки не треба поділити піцу на друзів."
        ],
        "життя": [
            "Коли прокинувся о шостій ранку, але це неділя — мінус довіра до життя.",
            "План на день: 1. Почати діяти. 2. Прокрастинувати. 3. Пожалкувати.",
            "Щастя — це коли вайфай підключився без пароля.",
            "Мій гаманець — як цибуля. Відкриєш — і плачеш.",
            "Кажуть, гроші не головне. Але чомусь без них голодно."
        ],
        "тварини": [
            "Кіт дивиться на тебе як на дурня, але з любов’ю.",
            "Собака не лає — просто перевіряє твоє терпіння.",
            "Якщо рибка мовчить, це не значить, що їй байдуже.",
            "Коли хом’як бігає в колесі, він думає, що качає карму.",
            "Папуга — це просто кіт, який знає більше слів."
        ]
    }

    # Перевіряємо, чи існує така категорія
    if category not in jokes:
        return "Такої категорії немає. Спробуй ще раз."

    # Вибираємо випадковий жарт
    return random.choice(jokes[category])


def show_categories():
    """
    Виводить список доступних категорій, щоб користувач знав, що можна вибрати.
    """
    print("\nДоступні категорії жартів:")
    print("• it — жарти про програмістів та технології")
    print("• школа — жарти про навчання")
    print("• життя — жарти про повсякденні ситуації")
    print("• тварини — жарти про домашніх улюбленців\n")


def main():
    """
    Основна функція, яка керує програмою.
    Вона повторює цикл:
    1. Показує категорії.
    2. Пропонує вибрати одну.
    3. Виводить випадковий жарт з цієї категорії.
    4. Дає можливість користувачу продовжити або вийти.
    """
    print("=== Генератор жартів з категоріями (v2.2) ===")

    while True:
        show_categories()
        category = input("Введи категорію (або 'вихід' щоб завершити): ").strip().lower()

        if category == "вихід":
            print("До зустрічі! Гарного настрою.")
            break

        print("\n" + get_random_joke(category))
        input("\nНатисни Enter, щоб побачити інший жарт...\n")


# ---------- ЗАПУСК ПРОГРАМИ ----------
if __name__ == "__main__":
    main()


